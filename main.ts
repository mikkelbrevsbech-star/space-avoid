info.onLifeZero(function () {
    let top_scores = settings.readNumberArray("leaderboard")
if (top_scores == null) {
        top_scores = []
    }
    // 2. Tilføj din nuværende score
    current_score = info.score()
    top_scores.push(current_score)
    top_scores.sort()
top_scores.reverse()
    // 4. Behold kun de 5 bedste
    if (top_scores.length > 5) {
        top_scores = top_scores.slice(0, 5)
    }
    settings.writeNumberArray("leaderboard", top_scores)
// 6. Lav teksten manuelt med en løkke (meget mere stabilt)
    leaderboard_text = "TOP 5 SCORES:"
    for (let score_val of top_scores) {
        leaderboard_text = "" + leaderboard_text + "\n" + ("" + score_val)
    }
    // 7. Vis og slut
    game.showLongText(leaderboard_text, DialogLayout.Center)
    game.gameOver(false)
})
